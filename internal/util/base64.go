package util

import (
	"encoding/base64"
	"strings"
)

// Base64Decode decodes a base64 string, tolerating both standard and URL
// alphabets and any amount of padding. It is useful for decoding values from
// external systems that are inconsistent about base64 variants.
func Base64Decode(s string) ([]byte, error) {
	s = strings.ReplaceAll(s, "+", "-")
	s = strings.ReplaceAll(s, "/", "_")
	s = strings.TrimRight(s, "=")
	return base64.RawURLEncoding.DecodeString(s)
}
